'''

O que é uma estrutura de repetição

--> Serve para executar um bloco de código várias vezes
--> ENQUANTO UMA CONDIÇÃO FOR VERDADEIRA
'''
conta = 5

while conta >= 0:
    print(conta)
    conta = conta - 1


#Tabuada

tabuada = 5
contador = 0

while contador <= 10:
    print(tabuada, "x", contador, "=", contador * tabuada)
    contador += 1
    
